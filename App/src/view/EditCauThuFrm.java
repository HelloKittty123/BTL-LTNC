    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        ClbList = ClLBController.findAll();
        boolean isOK = true;
        int soao = 0, madoi = 0;
        Date ngaysinh = null;
        String tendoi = null, tencauthu = null, vitri = null, linkanh = null;

        if (cbTenDoi.getSelectedIndex() >= 0) {
            tendoi = cbTenDoi.getSelectedItem().toString();
            for(CLB clb : ClbList) {
                if(clb.getTendoi().equals(tendoi)) {
                    madoi = clb.getMadoi();
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane,
                    "Bạn chưa chọn CLB");
            isOK = false;
        }
        if (jtfTenCauThu.getText().length() > 0 && jtfTenCauThu.getText() != null) {
            tencauthu = jtfTenCauThu.getText();
        } else {
            JOptionPane.showMessageDialog(rootPane,
                    "Bạn chưa nhập tên cầu thủ");
            isOK = false;
        }

        if (jtfNgaySinh.getText().length() > 0 && jtfNgaySinh.getText() != null) {
            try {
                ngaysinh = new SimpleDateFormat("dd/MM/yyyy").parse(jtfNgaySinh.getText());
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(rootPane,
                    "Bạn nhập sai định dạng, ngày sinh phải có định dạng dd/MM/yyyy");
                isOK = false;
            }
        } else {
            JOptionPane.showMessageDialog(rootPane,
                    "Bạn chưa nhập ngày sinh");
            isOK = false;
        }

        if (jtfSoao.getText().length() > 0 && jtfSoao.getText() != null) {
            try {
                soao = Integer.parseInt(jtfSoao.getText());
            } catch(Exception e) {
                JOptionPane.showMessageDialog(rootPane,
                    "Số áo phải là số không thể chứa kí tự khác");
                isOK = false;
            }
        } else {
            JOptionPane.showMessageDialog(rootPane,
                    "Bạn chưa nhập số áo");
            isOK = false;
        }
        
        if (jtfViTri.getText().length() > 0 && jtfViTri.getText() != null) {
            vitri = jtfViTri.getText();
        } else {
            JOptionPane.showMessageDialog(rootPane,
                    "Bạn chưa nhập vị trí");
            isOK = false;
        }
        
         if (jtfImage.getText().length() > 0 && jtfImage.getText() != null) {
            linkanh = jtfImage.getText();
        } else {
            JOptionPane.showMessageDialog(rootPane,
                    "Bạn chưa chọn ảnh cho cầu thủ");
            isOK = false;
        }

        if (isOK) {
            CauThu cauThu = new CauThu(madoi, macauthu, soao, ngaysinh, tencauthu, vitri, linkanh);
            JOptionPane.showMessageDialog(rootPane,
                    "Bạn đã cập nhật thành công cầu thủ");
            CauThuConTroller.update(cauThu);

            this.dispose();
        }
//        homeFrm = new HomeFrm();
//        homeFrm.ShowCauThu();
    }//GEN-LAST:event_btnSaveActionPerformed

