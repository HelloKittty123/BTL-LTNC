    void editCauThuData(CauThu cauThu) {
        showConboData();
        macauthu = cauThu.getMacauthu();
        cbTenDoi.setSelectedItem(cauThu.getTendoi());
        jtfTenCauThu.setText(cauThu.getTencauthu());
        jtfNgaySinh.setText(new SimpleDateFormat("dd/MM/yyyy").format(cauThu.getNgaysinh()));
        jtfSoao.setText(String.valueOf(cauThu.getSoao()));
        jtfViTri.setText(cauThu.getVitri());
        jtfImage.setText(cauThu.getLinkImage());
        
        File file = new File(cauThu.getLinkImage());
        // lấy đường dẫn file
        String pathFile = file.getAbsolutePath();
        BufferedImage b;
        try {
            b = ImageIO.read(file);
            jlbImage.setIcon(new ImageIcon(b.getScaledInstance(200, 200, Image.SCALE_SMOOTH)));
        } catch (IOException ex) {
            ex.printStackTrace();
        }
    }
    
    
    public void showConboData() {
        ClbList = ClLBController.findAll();
        cbTenDoi.removeAllItems();
        ClbList.forEach(clb -> {
            cbTenDoi.addItem(clb.getTendoi());
        });
    }
