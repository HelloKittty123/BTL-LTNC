    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        boolean isOK = true;
        int socauthu = 0;
        String tendoi = null, doitruong = null, HLV = null, linkanh = null;

        socauthu = Integer.valueOf(jtfCount.getText());
        
        if (jtfTenDoi.getText().length() > 0 && jtfTenDoi.getText() != null) {
            tendoi = jtfTenDoi.getText();
        } else {
            JOptionPane.showMessageDialog(rootPane,
                    "Bạn chưa nhập tên đội");
            isOK = false;
        }

        if (jtfDoiTruong.getText().length() > 0 && jtfDoiTruong.getText() != null) {
            doitruong = jtfDoiTruong.getText();
        } else {
            JOptionPane.showMessageDialog(rootPane,
                    "Bạn chưa nhập tên đội trưởng");
            isOK = false;
        }

        if (jtfHLV.getText().length() > 0 && jtfHLV.getText() != null) {
            HLV = jtfHLV.getText();
        } else {
            JOptionPane.showMessageDialog(rootPane,
                    "Bạn chưa nhập tên huấn luyện viên");
            isOK = false;
        }
        
        if (jtfImage.getText().length() > 0 && jtfImage.getText() != null) {
            linkanh = jtfImage.getText();
        } else {
            JOptionPane.showMessageDialog(rootPane,
                    "Bạn chưa chọn ảnh cho đội");
            isOK = false;
        }
        
        if (isOK) {
            CLB clb = new CLB(madoi, socauthu, tendoi, doitruong, HLV, linkanh);
            JOptionPane.showMessageDialog(rootPane,
                    "Bạn đã cập nhật CLB thành công");
            ClLBController.update(clb);
            this.dispose();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

